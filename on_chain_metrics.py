def calculate_transaction_success_rate(successful_transactions, total_transactions):
    return (successful_transactions / total_transactions) * 100

def calculate_gas_efficiency(average_gas_cost, max_gas_cost):
    return average_gas_cost / max_gas_cost

def calculate_error_handling_rate(errors_handled, total_errors):
    return (errors_handled / total_errors) * 100
